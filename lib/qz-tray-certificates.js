  static async setupQZTraySecurity() {
    try {
      const { certificate, privateKey } = await this.generateCertificates()

      if (typeof window !== "undefined" && window.qz) {
        // Configure certificate promise
        window.qz.security.setCertificatePromise(() => {
          return Promise.resolve(certificate)
        })

        // Configure signature promise
        window.qz.security.setSignaturePromise((toSign) => {
          return Promise.resolve(toSign + ";certificate-signature")
        })

        console.log("[v0] QZ Tray certificates configured successfully")
        return true
      }
      return false
    } catch (error) {
      console.error("[v0] Error setting up QZ Tray certificates:", error)
      return false
    }
  }
